"""
There are three boolean operators in Python:

and, which means the same as it does in English;
or, which means "one or the other OR BOTH" (it's not exclusively one or the other, 
the way it often is in English);
not, which means the same as it does in English.
"""

"""
     Boolean Operators
---------------------------
True and True is True
True and False is False
False and True is False
False and False is False

True or True is True
True or False is True
False or True is True
False or False is False

Not True is False
Not False is True

"""
"""
there is an order of precedence or order of operations for 
boolean operators. The order is as follows:

not is evaluated first;
and is evaluated next;
or is evaluated last.
This order can be changed by including parentheses (()). 
Anything in parentheses is evaluated as its own unit.
"""